package hongongja.OOP;

public class Printer {
    int println(int num){
        System.out.println(num);
        return num;
    }
    boolean println(boolean trueOrFalse){
        System.out.println(trueOrFalse);
        return trueOrFalse;
    }
    double println(double num){
        System.out.println(num);
        return num;
    }
    String println(String name){
        System.out.println(name);
        return name;
    }
}
